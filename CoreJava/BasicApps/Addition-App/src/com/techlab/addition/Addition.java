package com.techlab.addition;

public class Addition {
	public int addition(int firstno, int secondno) {
		return (firstno + secondno);
	}

	public float addition(float firstno, float secondno) {
		return (firstno + secondno);
	}

	public double addition(double firstno, double secondno) {
		return (firstno + secondno);
	}

	public String addition(String firstno, String secondno) {
		return (firstno + secondno);
	}

}
